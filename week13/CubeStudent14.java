package week13;

public class CubeStudent14 {
    static int area(int x)  {
        return 6 * x * x;
    }

    static int volume(int x) {
        return x * x * x;
    }

    public static void main(String[] args) {
        System.out.println(area(10));
        System.out.println(volume(10));   
    }
}
