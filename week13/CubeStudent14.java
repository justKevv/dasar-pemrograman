package week13;

public class CubeStudent14 {
    static int area(int x, int y)  {
        return x * y;
    }

    static int volume(int x, int y, int z) {
        return x * y * z;
    }

    public static void main(String[] args) {
        System.out.println(area(10, 20));
        System.out.println(volume(10, 20, 30));   
    }
}
